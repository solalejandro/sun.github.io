const KEY = "jb_lang";
let current = localStorage.getItem(KEY) || (navigator.language || "es").slice(0, 2);
if (!STRINGS[current]) current = "es";

export function getLang() { return current; }

export function setLang(lang) {
  if (!STRINGS[lang]) return;
  current = lang;
  localStorage.setItem(KEY, lang);
  document.documentElement.lang = lang;
  applyStaticTranslations();
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
}

/** Traduce una clave; admite valores objeto {es,en}. */
export function t(key) {
  const dict = STRINGS[current] || STRINGS.es;
  return dict[key] ?? STRINGS.es[key] ?? key;
}

/** Devuelve el texto localizado de un campo {es,en} o string. */
export function loc(val) {
  if (val == null) return "";
  if (typeof val === "string") return val;
  return val[current] ?? val.es ?? val.en ?? "";
}

/** Aplica traducciones a elementos con data-i18n (header/footer estáticos). */
export function applyStaticTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
  document.querySelectorAll(".lang-btn").forEach((b) => {
    b.classList.toggle("active", b.dataset.lang === current);
  });
}
